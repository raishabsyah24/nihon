List<KanaCharacter> localKana(String type) {
  final source = type == 'HIRAGANA'
      ? [..._hiragana, ..._hiraganaDakuten, ..._hiraganaHandakuten]
      : [..._katakana, ..._katakanaDakuten, ..._katakanaHandakuten];
  return [
    for (final row in source)
      KanaCharacter(
        id: '${type.toLowerCase()}-${row.$2}',
        type: type,
        character: row.$1,
        romaji: row.$2,
        example: row.$3,
        strokeSteps: _placeholderStrokeSteps(row.$1),
      ),
  ];
}

List<StrokeStep> _placeholderStrokeSteps(String character) {
  final count = switch (character) {
    'い' || 'こ' || '二' || 'ニ' => 2,
    'あ' || 'お' || 'き' || 'さ' || 'む' || 'ア' || 'オ' || 'キ' => 3,
    'ぬ' || 'ね' || 'め' || 'カ' || 'タ' || 'ホ' || 'ミ' => 4,
    _ => 2,
  };

  return [
    for (var i = 1; i <= count; i++)
      StrokeStep(
        step: i,
        note:
            'Latihan stroke $i untuk $character. Data animasi asli bisa diganti dari admin/API.',
      ),
  ];
}
